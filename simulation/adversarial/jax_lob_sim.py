# Batch / parallel execution (simulated vmap)
# ---------------------------------------------------------------------------


def run_parallel_books(
    n_books: int,
    messages_per_book: Sequence[Sequence[OrderMessage]],
    *,
    n_orders: int = 100,
    n_trades: int = 100,
) -> BatchResult:
    """Run multiple independent books in parallel (simulated vmap).

    Mirrors JAX-LOB's vmap pattern where the same message processing
    logic is vectorized across thousands of independent books on GPU.
    In pure Python we process sequentially but maintain the same
    interface for drop-in JAX acceleration.

    Args:
        n_books: Number of parallel books.
        messages_per_book: Message sequences for each book.
        n_orders: Max orders per side per book.
        n_trades: Max trades per book.

    Returns:
        BatchResult with final states, digests, and L2 snapshots.
    """
    if len(messages_per_book) != n_books:
        raise JaxLobSimError(
            f"messages_per_book length {len(messages_per_book)} != n_books {n_books}"
        )

    final_states: list[BookState] = []
    digests: list[str] = []
    l2_snapshots: list[L2Snapshot] = []

    for msgs in messages_per_book:
        state = make_book_state(n_orders=n_orders, n_trades=n_trades)
        process_message_batch(state, msgs)
        final_states.append(state)
        digests.append(compute_state_digest(state))
        l2_snapshots.append(get_l2_snapshot(state))

    return BatchResult(
        final_states=tuple(final_states),
        digests=tuple(digests),
        l2_snapshots=tuple(l2_snapshots),
    )


