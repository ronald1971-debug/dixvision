@runtime_checkable
class ArrayBackend(Protocol):
    """Protocol for array operations — default is pure Python lists."""

    def zeros(self, shape: tuple[int, int]) -> list[list[int]]: ...

    def process(self, state: BookState, msg: OrderMessage) -> BookState: ...


# ---------------------------------------------------------------------------
# Value objects (mirrors JAX-LOB's LobState NamedTuple + msg array)
# ---------------------------------------------------------------------------


class OrderMessage(NamedTuple):
    """Single order message matching JAX-LOB's 8-column format.

    Fields:
        msg_type: 1=limit, 2=cancel, 3=market
        side: 0=ask, 1=bid
        quantity: order quantity
        price: order price (integer ticks)
        order_id: unique order identifier
        trader_id: trader identifier
        time_s: timestamp seconds
        time_ns: timestamp nanoseconds
    """

    msg_type: int
    side: int
    quantity: int
    price: int
    order_id: int
    trader_id: int
    time_s: int
    time_ns: int


@dataclasses.dataclass(frozen=True, slots=True)
class TradeRecord:
    """Single trade generated by matching.

    Matches JAX-LOB's trade array format:
    [price, quantity, resting_order_id, aggressor_order_id, time_s, time_ns]
    """

    price: int
    quantity: int
    resting_order_id: int
    aggressor_order_id: int
    time_s: int
    time_ns: int


@dataclasses.dataclass(slots=True)
class BookState:
    """Mutable order book state mirroring JAX-LOB's LobState.

    Each side is a list of orders, where each order is:
    [price, quantity, order_id, trader_id, time_s, time_ns]
    matching JAX-LOB's 6-column orderside array.
    """

    asks: list[list[int]]
    bids: list[list[int]]
    trades: list[TradeRecord]
    n_orders: int
    n_trades: int

    def copy(self) -> BookState:
        return BookState(
            asks=[row[:] for row in self.asks],
            bids=[row[:] for row in self.bids],
            trades=list(self.trades),
            n_orders=self.n_orders,
            n_trades=self.n_trades,
        )


@dataclasses.dataclass(frozen=True, slots=True)
class L2Snapshot:
    """Level-2 book snapshot (top N price levels aggregated).

    Mirrors JAX-LOB's ``get_L2_state`` output.
    """

    best_bid: int
    best_ask: int
    bid_levels: tuple[tuple[int, int], ...]  # (price, total_qty)
    ask_levels: tuple[tuple[int, int], ...]  # (price, total_qty)
    spread: int
    mid_price_x2: int  # 2 * mid_price to avoid float (integer ticks)


@dataclasses.dataclass(frozen=True, slots=True)
class BatchResult:
    """Result of running multiple books in parallel (simulated vmap).

    Attributes:
        final_states: Final book states for each parallel book.
        digests: BLAKE2b digests per book for INV-15.
        l2_snapshots: Final L2 snapshots per book.
    """

    final_states: tuple[BookState, ...]
    digests: tuple[str, ...]
    l2_snapshots: tuple[L2Snapshot, ...]


