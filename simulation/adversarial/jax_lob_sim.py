# ADAPTED FROM: KangOxford/jax-lob
# (gymnax_exchange/jaxob/JaxOrderBookArrays.py — add_order, cancel_order, match_order;
#  gymnax_exchange/jaxob/jorderbook.py — OrderBook class, LobState NamedTuple,
#  process_orders_array, get_best_bid/ask, get_L2_state;
#  gymnax_exchange/jaxob/JaxOrderBookArrays.py — scan_through_entire_array,
#  cond_type_side, get_volume_at_price.)
"""C-46 — Vectorized LOB simulation for batch adversarial evaluation.

This module adapts the JAX-LOB project
(https://github.com/KangOxford/jax-lob, MIT License) as a batch LOB
simulator for adversarial scenario evaluation. JAX-LOB's design uses
JAX jit+vmap to process thousands of order books in parallel on GPU;
this adaptation reproduces the *architecture* (array-backed order books,
functional state transitions, batch processing) in pure Python so the
module is importable without JAX installed.

What survives from upstream (KangOxford/jax-lob):

* **LobState as a NamedTuple** — ``jorderbook.py`` line 15: the order
  book state is a named triple ``(asks, bids, trades)`` where each
  element is a fixed-size 2D array. We reproduce this as a frozen
  dataclass with list-of-lists backing.
* **Functional order processing** — ``JaxOrderBookArrays.py``
  ``add_order``, ``cancel_order``, ``match_order`` are standalone
  functions that take the state and a message array and return new state.
  We mirror this with ``process_message()`` operating on ``BookState``.
* **Batch scanning** — ``scan_through_entire_array`` processes an Nx8
  message array sequentially through the book. We reproduce this as
  ``process_message_batch()`` (sequential) and ``run_parallel_books()``
  (simulated vmap over multiple independent books).
* **Order format** — each order is [type, side, quantity, price,
  order_id, trader_id, time_s, time_ns] matching JAX-LOB's int32
  8-column convention.

What we replaced:

* JAX ``jnp.array`` and ``@jax.jit`` → plain Python lists and loops.
  The Protocol seam ``ArrayBackend`` allows optional JAX acceleration
  via lazy import (tested by injecting a fake).
* ``jax.lax.scan`` → explicit Python loop in ``process_message_batch``.
* ``jax.vmap`` → ``run_parallel_books()`` using sequential iteration
  (or ThreadPoolExecutor in production with injected parallelism).

DIX integration rules:

* OFFLINE simulation tier only. No IO, no clock reads.
* Deterministic: same message sequence → same final state.
* INV-15: BLAKE2b digest computed over book state for replay.
* No JAX import at module level — hidden behind Protocol seam.
"""

from __future__ import annotations

import dataclasses
import hashlib
from collections.abc import Sequence
from typing import Final, NamedTuple, Protocol, runtime_checkable

JAX_LOB_SIM_VERSION: Final[str] = "c-46.v1"
NEW_PIP_DEPENDENCIES: tuple[str, ...] = ()

# ---------------------------------------------------------------------------
# Constants matching JAX-LOB conventions
# ---------------------------------------------------------------------------

INIT_ID: Final[int] = -9000
"""Sentinel for initial orders without explicit ID (JAX-LOB INITID)."""

EMPTY: Final[int] = -1
"""Empty slot sentinel (JAX-LOB uses -1 for empty array positions)."""

# Message type codes (matching JAX-LOB inttype mapping in jorderbook.py:60-73)
MSG_LIMIT: Final[int] = 1
MSG_CANCEL: Final[int] = 2
MSG_MARKET: Final[int] = 3

# Side codes
SIDE_ASK: Final[int] = 0
SIDE_BID: Final[int] = 1


# ---------------------------------------------------------------------------
# Protocol seam for optional JAX acceleration
