def make_book_state(n_orders: int = 100, n_trades: int = 100) -> BookState:
    """Create empty book state (mirrors OrderBook.init in jorderbook.py:31-35)."""
    return BookState(
        asks=[],
        bids=[],
        trades=[],
        n_orders=n_orders,
        n_trades=n_trades,
    )


def _add_order(side_orders: list[list[int]], msg: OrderMessage) -> None:
    """Add a limit order to the given side (mirrors JaxOrderBookArrays.add_order).

    Orders are inserted in price-time priority:
    - Asks: ascending price, then ascending time
    - Bids: descending price, then ascending time
    """
    order = [msg.price, msg.quantity, msg.order_id, msg.trader_id, msg.time_s, msg.time_ns]
    side_orders.append(order)


def _cancel_order(side_orders: list[list[int]], msg: OrderMessage) -> None:
    """Cancel/reduce quantity (mirrors JaxOrderBookArrays.cancel_order).

    Finds order by order_id first, falls back to price match with INIT_ID
    (same as JAX-LOB's cancel_order fallback logic).
    """
    # First try exact order_id match
    for i, order in enumerate(side_orders):
        if order[2] == msg.order_id:
            order[1] -= msg.quantity
            if order[1] <= 0:
                side_orders.pop(i)
            return

    # Fallback: match by price with init IDs (JAX-LOB init_id_match)
    for i, order in enumerate(side_orders):
        if order[0] == msg.price and order[2] <= INIT_ID:
            order[1] -= msg.quantity
            if order[1] <= 0:
                side_orders.pop(i)
            return


def _get_top_bid_idx(bids: list[list[int]]) -> int | None:
    """Get index of best bid (highest price, earliest time).

    Mirrors JaxOrderBookArrays.__get_top_bid_order_idx.
    """
    if not bids:
        return None
    best_idx = 0
    best_price = bids[0][0]
    best_time_s = bids[0][4]
    best_time_ns = bids[0][5]
    for i in range(1, len(bids)):
        p, _, _, _, ts, tns = bids[i]
        if p > best_price or (
            p == best_price and (ts < best_time_s or (ts == best_time_s and tns < best_time_ns))
        ):
            best_idx = i
            best_price = p
            best_time_s = ts
            best_time_ns = tns
    return best_idx


def _get_top_ask_idx(asks: list[list[int]]) -> int | None:
    """Get index of best ask (lowest price, earliest time).

    Mirrors JaxOrderBookArrays.__get_top_ask_order_idx.
    """
    if not asks:
        return None
    best_idx = 0
    best_price = asks[0][0]
    best_time_s = asks[0][4]
    best_time_ns = asks[0][5]
    for i in range(1, len(asks)):
        p, _, _, _, ts, tns = asks[i]
        if p < best_price or (
            p == best_price and (ts < best_time_s or (ts == best_time_s and tns < best_time_ns))
        ):
            best_idx = i
            best_price = p
            best_time_s = ts
            best_time_ns = tns
    return best_idx


def _match_order(
    side_orders: list[list[int]],
    top_idx: int,
    msg: OrderMessage,
    trades: list[TradeRecord],
) -> int:
    """Match against top order, return remaining quantity.

    Mirrors JaxOrderBookArrays.match_order: reduces resting order qty,
    generates trade record, removes if fully filled.
    """
    resting = side_orders[top_idx]
    fill_qty = min(msg.quantity, resting[1])

    trade = TradeRecord(
        price=resting[0],
        quantity=fill_qty,
        resting_order_id=resting[2],
        aggressor_order_id=msg.order_id,
        time_s=msg.time_s,
        time_ns=msg.time_ns,
    )
    trades.append(trade)

    resting[1] -= fill_qty
    if resting[1] <= 0:
        side_orders.pop(top_idx)

    return msg.quantity - fill_qty


def process_message(state: BookState, msg: OrderMessage) -> BookState:
    """Process a single message against the book state.

    Mirrors JAX-LOB's cond_type_side dispatch logic in
    JaxOrderBookArrays.py which routes based on msg_type and side.
    """
    if msg.msg_type == MSG_LIMIT:
        if msg.side == SIDE_BID:
            # Check if crosses best ask (marketable)
            remaining = msg.quantity
            while remaining > 0:
                ask_idx = _get_top_ask_idx(state.asks)
                if ask_idx is None:
                    break
                if state.asks[ask_idx][0] > msg.price:
                    break
                fill_msg = msg._replace(quantity=remaining)
                remaining = _match_order(state.asks, ask_idx, fill_msg, state.trades)
            if remaining > 0:
                rest_msg = msg._replace(quantity=remaining)
                _add_order(state.bids, rest_msg)
        else:
            # ASK limit
            remaining = msg.quantity
            while remaining > 0:
                bid_idx = _get_top_bid_idx(state.bids)
                if bid_idx is None:
                    break
                if state.bids[bid_idx][0] < msg.price:
                    break
                fill_msg = msg._replace(quantity=remaining)
                remaining = _match_order(state.bids, bid_idx, fill_msg, state.trades)
            if remaining > 0:
                rest_msg = msg._replace(quantity=remaining)
                _add_order(state.asks, rest_msg)

    elif msg.msg_type == MSG_CANCEL:
        if msg.side == SIDE_BID:
            _cancel_order(state.bids, msg)
        else:
            _cancel_order(state.asks, msg)

    elif msg.msg_type == MSG_MARKET:
        # Market order: match against opposite side until filled
        remaining = msg.quantity
        if msg.side == SIDE_BID:
            # Market buy matches against asks
            while remaining > 0:
                ask_idx = _get_top_ask_idx(state.asks)
                if ask_idx is None:
                    break
                fill_msg = msg._replace(quantity=remaining)
                remaining = _match_order(state.asks, ask_idx, fill_msg, state.trades)
        else:
            # Market sell matches against bids
            while remaining > 0:
                bid_idx = _get_top_bid_idx(state.bids)
                if bid_idx is None:
                    break
                fill_msg = msg._replace(quantity=remaining)
                remaining = _match_order(state.bids, bid_idx, fill_msg, state.trades)

    return state


def process_message_batch(
    state: BookState,
    messages: Sequence[OrderMessage],
) -> BookState:
    """Process array of messages sequentially.

    Mirrors JAX-LOB's scan_through_entire_array which uses
    jax.lax.scan to fold messages through the book state.
    """
    for msg in messages:
        process_message(state, msg)
    return state


def get_best_bid(state: BookState) -> int | None:
    """Get best bid price (mirrors jorderbook.py get_best_bid)."""
    idx = _get_top_bid_idx(state.bids)
    if idx is None:
        return None
    return state.bids[idx][0]


def get_best_ask(state: BookState) -> int | None:
    """Get best ask price (mirrors jorderbook.py get_best_ask)."""
    idx = _get_top_ask_idx(state.asks)
    if idx is None:
        return None
    return state.asks[idx][0]


def get_l2_snapshot(state: BookState, n_levels: int = 5) -> L2Snapshot:
    """Get Level-2 snapshot (mirrors jorderbook.py get_L2_state).

    Aggregates quantity at each price level, returns top N levels
    per side.
    """
    # Aggregate bids by price (descending)
    bid_agg: dict[int, int] = {}
    for order in state.bids:
        bid_agg[order[0]] = bid_agg.get(order[0], 0) + order[1]
    bid_levels = sorted(bid_agg.items(), key=lambda x: -x[0])[:n_levels]

    # Aggregate asks by price (ascending)
    ask_agg: dict[int, int] = {}
    for order in state.asks:
        ask_agg[order[0]] = ask_agg.get(order[0], 0) + order[1]
    ask_levels = sorted(ask_agg.items(), key=lambda x: x[0])[:n_levels]

    best_bid = bid_levels[0][0] if bid_levels else 0
    best_ask = ask_levels[0][0] if ask_levels else 0
    spread = (best_ask - best_bid) if (best_bid and best_ask) else 0
    mid_x2 = (best_bid + best_ask) if (best_bid and best_ask) else 0

    return L2Snapshot(
        best_bid=best_bid,
        best_ask=best_ask,
        bid_levels=tuple(bid_levels),
        ask_levels=tuple(ask_levels),
        spread=spread,
        mid_price_x2=mid_x2,
    )


def compute_state_digest(state: BookState) -> str:
    """BLAKE2b digest of book state for INV-15 replay verification."""
    h = hashlib.blake2b(digest_size=16)
    h.update(len(state.bids).to_bytes(4, "little"))
    h.update(len(state.asks).to_bytes(4, "little"))
    h.update(len(state.trades).to_bytes(4, "little"))
    for order in sorted(state.bids, key=lambda o: (o[0], o[4], o[5], o[2])):
        for v in order:
            h.update(v.to_bytes(8, "little", signed=True))
    for order in sorted(state.asks, key=lambda o: (o[0], o[4], o[5], o[2])):
        for v in order:
            h.update(v.to_bytes(8, "little", signed=True))
    for t in state.trades:
        h.update(t.price.to_bytes(8, "little", signed=True))
        h.update(t.quantity.to_bytes(8, "little", signed=True))
        h.update(t.resting_order_id.to_bytes(8, "little", signed=True))
        h.update(t.aggressor_order_id.to_bytes(8, "little", signed=True))
    return h.hexdigest()


